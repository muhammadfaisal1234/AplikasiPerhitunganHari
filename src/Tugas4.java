// Method to initialize the JComboBox and JSpinner event listeners
private void initializeCalendarBindings() {
    // Set the initial year in the JSpinner to the current year
    jSpinner1.setValue(LocalDate.now().getYear());

    // Listener for the JComboBox (month selector)
    jComboBox1.addActionListener(evt -> updateCalendars());

    // Listener for the JSpinner (year selector)
    jSpinner1.addChangeListener(e -> updateCalendars());
}

// Method to update both jCalendar1 and jCalendar2 to the selected month and year
private void updateCalendars() {
    try {
        int year = (int) jSpinner1.getValue();
        int month = jComboBox1.getSelectedIndex() + 1; // January = 0, so +1 to get the correct month number

        // Set the first day of the selected month and year in jCalendar1 and jCalendar2
        LocalDate selectedDate = LocalDate.of(year, month, 1);
        java.util.Date calendarDate = java.sql.Date.valueOf(selectedDate);

        jCalendar1.setDate(calendarDate);
        jCalendar2.setDate(calendarDate);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengupdate kalender!", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
