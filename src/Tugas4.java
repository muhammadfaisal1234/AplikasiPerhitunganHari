    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        int year = (int) jSpinner1.getValue();
        String monthString = (String) jComboBox1.getSelectedItem();
        int month = jComboBox1.getSelectedIndex() + 1; // January = 0, so +1 to get correct month number

        LocalDate date = LocalDate.of(year, month, 1);

        // Calculate days in the month
        int daysInMonth = date.lengthOfMonth();
        boolean isLeapYear = date.isLeapYear();
        String firstDay = date.getDayOfWeek().toString();
        String lastDay = date.withDayOfMonth(daysInMonth).getDayOfWeek().toString();

        // Display results
        jLabel3.setText("Hasil Jumlah Hari : " + daysInMonth);
        jLabel4.setText("Tahun Kabisat : " + (isLeapYear ? "Ya" : "Tidak"));
        jLabel5.setText("Hari Pertama Dalam Bulan : " + firstDay);
        jLabel6.setText("Hari Terakhir Dalam Bulan : " + lastDay);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Pastikan tahun dan bulan dipilih dengan benar!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      try {
        LocalDate startDate = LocalDate.ofInstant(jCalendar1.getDate().toInstant(), java.time.ZoneId.systemDefault());
        LocalDate endDate = LocalDate.ofInstant(jCalendar2.getDate().toInstant(), java.time.ZoneId.systemDefault());

        long daysBetween = ChronoUnit.DAYS.between(startDate, endDate);

        JOptionPane.showMessageDialog(this, "Selisih Hari: " + daysBetween + " hari", "Hasil", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Pastikan kedua tanggal dipilih dengan benar!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

